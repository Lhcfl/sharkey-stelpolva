function createRendererInstance(game: DropAndFusionGame) {
	return Matter.Render.create({
		engine: game.engine,
		canvas: canvasEl.value!,
		options: {
			width: game.GAME_WIDTH,
			height: game.GAME_HEIGHT,
			background: 'transparent', // transparent to hide
